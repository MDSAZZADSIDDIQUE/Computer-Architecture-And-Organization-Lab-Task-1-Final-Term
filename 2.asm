.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC FAR
LEA AX, @DATA
MOV DS, AX
MOV AH, 1
INT 21H
MOV BL, AL
CMP AL, 65
JE PRINT_CARRIAGE_RETURN
CMP AL, 66
JE PRINT_LINE_FEED
JMP EXIT
PRINT_CARRIAGE_RETURN:
MOV AH, 2
MOV DL, 13
INT 21H
JMP EXIT
PRINT_LINE_FEED:
MOV AH, 2
MOV DL, 10
INT 21H
JMP EXIT
EXIT:
MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN